name = input("Enter a name: ")
name = name.lower()

letter_count = {}
for letter in name:
    if letter.isalpha():
        if letter in letter_count:
            letter_count[letter] += 1
        else:
            letter_count[letter] = 1

for letter, count in letter_count.items():
    print(f"The alphabet '{letter}' appears {count} time(s) in the name.")
